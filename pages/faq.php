    <p>
        <h3><a name="#missing-routing-group" class="faq-question-title">Why can't I find a specific request type in correspondence I'm routing?</a></h3>
        <span>This is caused when routing chains aren't properly configured. Only routing chains with at least one routing group will appear in the list of available request types.</span>
    </p>

    <p>
        <h3><a name="dark-mode" class="faq-question-title">How do I enable dark mode in CTS?</a></h3>
        <span>Click Start ( <i class="fab fa-windows"></i> ), type "color settings", and then choose the <em>Color settings</em> item. In the <em>Color settings</em> control panel, find the option that says <em>Choose your color</em> and select <em>Dark</em>. This will enable dark mode for your entire system, including CTS.</span>
    </p>

