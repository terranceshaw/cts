    <h1 class="page-title">User Guide</h1>
    <span class="subtitle">Get to know the CTS web app or contact the administrator.</span>
    
    <div class="guide-section">
        <div class="guide-section-title">Routing Chains / Correspondence Types</div>
        <div class="guide-section-content">
            <p>Routing chains are represented as "Correspondence Type"s when you route new correspondence, and are the CTS equivalent of the chop chains from the command's routing matrix. Routing chains use <strong>routing groups</strong>, which have users assigned to them to establish the routing pipeline.</p>

            <blockquote><strong>Example:</strong> You have YN1 Johnson, YN3 Smith, and YNC Black from Combat Systems, Personnel, and XO Admin, respectively. You can add them to their respective groups (e.g., Department Yeoman, Personnel, and XO Admin) so that they are able to take action on correspondence that comes through their office.</blockquote>
        </div>
    </div>

    <div class="guide-section">
        <div class="guide-section-title">Routing Groups</div>
        <div class="guide-section-content">
            <p>Routing groups are the means of assigning user accounts to specific stops along a correspondence's items route. Routing groups can also be considered "titles"; for exmaple: a routing group could be named "LCPO", and any users assigned to that group would be able to take action on the correspondence as it progresses through the chop chain.</p>
        </div>
    </div>
